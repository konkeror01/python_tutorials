first_name = "Albert"
last_name = "Nzayinsenga"
age = 32

print("My name is " + first_name + " " + last_name + ". My age is " + str(age) + ".")  # Concatenation Method

print("My name is {} {}. My age is {}.".format(first_name, last_name, age))  # .format() method
# {} -> Curly brackets are called as placeholders in Python.

print(f"My name is {first_name} {last_name}. My age is {age}.")  # F String Method
