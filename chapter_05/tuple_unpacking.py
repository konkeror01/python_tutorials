# tuple unpacking :
piano_players = ('alen mark', 'john doe', 'joe hockey')
piano_players1, piano_players2, piano_players3 = piano_players

# you will need to create 3 variables as there are 3 elements.
# the first element of the tuple will be stored in the first variable.
# the second element of the tuple will be stored in the second variable.
# the third element of the tuple will be stored in the third variable.
print(piano_players1)
print(piano_players2)
print(piano_players3)
print(piano_players)
